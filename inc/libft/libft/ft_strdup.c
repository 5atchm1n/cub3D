#include "libft.h"

char		*ft_strdup(const char *src)
{
	size_t	i;
	char	*dup;

	i = 0;
	if (!(dup = malloc(sizeof(char) * (ft_strlen(src) + 1))))
		return (NULL);
	while (src[i] != '\0')
	{
		dup[i] = src[i];
		i++;
	}
	dup[i] = '\0';
	return (dup);
}
